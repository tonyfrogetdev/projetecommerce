    //         new Assert\Length(['min'=>3, 'max'=>255, 'minMessage'=>'Le nom du produit doit contenir au moins 3 caractères'])
    //     ]);
    //     $metadata->addPropertyConstraint('price', new Assert\NotBlank(['message'=>'Le prix du produit est obligatoire']));
